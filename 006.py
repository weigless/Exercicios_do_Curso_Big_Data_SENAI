#Exercicio 06: Escreva um programa que leia 6 notas diferentes e faça a média do aluno

print("***** CALCULADORA DE MÉDIA DAS NOTAS *****")
nota01= float(input("Digite a primeira nota: "))
nota02= float(input("Digite a segunda nota: "))
nota03= float(input("Digite a terceira nota: "))
nota04= float(input("Digite a quarta nota: "))
nota05= float(input("Digite a quinta nota: "))
nota06= float(input("Digite a sexta nota: "))
media= (nota01+nota02+nota03+nota04+nota05+nota06)/6

print(f"A sua média final é: {media}.")